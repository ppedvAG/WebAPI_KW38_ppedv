using Microsoft.EntityFrameworkCore;
using MovieApp.ServiceAPI.Data;

namespace MovieApp.ServiceAPI
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.

            builder.Services.AddControllers();
            // Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
            builder.Services.AddEndpointsApiExplorer();
            builder.Services.AddSwaggerGen();
            
            //Hier legen wir den MovieDbContext in den IOC Container
            builder.Services.AddDbContext<MovieDbContext>(options =>
            {
                options.UseSqlServer(builder.Configuration.GetConnectionString("MovieDbConnectionStringEntry"));
            });

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (app.Environment.IsDevelopment())
            {
                app.UseSwagger();
                app.UseSwaggerUI();
            }

            app.UseHttpsRedirection();

            app.UseAuthorization();


            app.MapControllers();

            app.Run();
        }
    }
}